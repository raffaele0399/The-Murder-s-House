     public void stampaInventario(Giocatore giocatore){
        String[] oggettiInventario = {"Collana", "Chiave dell'ingresso", "Chiave della cella", "Pistola", "Corda", "Fotografia"};
        
        for(Oggetto o : giocatore.getInventarioGiocatore().getInventario()){
            if(oggettiInventario[0].equals(o.getNome())) { //aggiungere controllo se Ã¨ stato preso o gettato
                lableCollana.setIcon(o.getIcona());
            }
            if(oggettiInventario[1].equals(o.getNome())) {
                lableChiaveUscita.setIcon(o.getIcona());
            }
            if(oggettiInventario[2].equals(o.getNome())) {
                lableChiaveCella.setIcon(o.getIcona());
            }
            if(oggettiInventario[3].equals(o.getNome())) {
                lablePistola.setIcon(o.getIcona());
            }
            if(oggettiInventario[4].equals(o.getNome())) {
                lableScala.setIcon(o.getIcona());
            }
            if(oggettiInventario[5].equals(o.getNome())) {
                lableFotografia.setIcon(o.getIcona());
            }
        }
    }

